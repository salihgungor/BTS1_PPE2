	<div class="blocks">
		<h1>Découvrez nos urgences</h1>
		<p>Au sein de notre service d’urgence, les patients et les accompagnants 
			sont accueillis de façon personnalisée par une équipe soignante dédiée 
			qui oriente les patients dans le secteur de soins adéquat après avoir 
			évalué le degré d’urgence. Pendant la prise en charge des patients dans 
			nos locaux, un ou une infirmier ou infirmière de coordination renseigne 
			les accompagnants présents en salle d’attente et fait le lien avec 
			l’équipe soignante.</p>
	</div>
	<div class="blocks">
		<h1> Disponibilitées de nos urgences </h1>
		<p>Notre service des Urgences accueille 24h/24, tous les jours de l’année,
			toute personne présentant une situation d’urgence médico-chirurgicale, 
			psychiatrique ou sociale.Notre service ne prend en charge que les 
			adultes.</p>
