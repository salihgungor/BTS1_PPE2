		<h1>Chirurgie orthopédique et traumatologique</h1>
		<p>Partie de la chirurgie qui traite les déformations des os, 
			des articulations, des muscles et des tendons et répare ceux qui sont abîmés
			au cours d'un accident (traumatisme).Chirurgie restauratrice osseuse, 
			articulaire, ligamentaire et musculo-tendineuse lors des traumatismes ou 
			à la suite de maladies de l'appareil locomoteur.Chirurgie prothétique de la 
			hanche, du genou et autres articulations.Chirurgie arthroscopique 
			(vidéo-assistée) en particulier du genou.Chirurgie réparatrice des 
			fractures ostéo-articulaires.</p>
