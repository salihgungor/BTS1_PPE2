		<h1>Chirurgie Digestive</h1>
		<p>La chirurgie digestive concerne les interventions réalisées sur l'appareil 
			digestif ciblant l'œsophage, l'estomac, le duodénum, l'intestin grêle, le 
			côlon et le rectum. Le chirurgien digestif réalise aussi des interventions 
			sur les voies biliaires et la vésicule, le foie, le pancréas, la rate et sur
			la paroi abdominale, telle que la chirurgie de la hernie inguinale, 
			expliquant l'intitulé chirurgie viscérale et digestive, le plus fréquemment 
			utilisé en France.La chirurgie hépatique lourde, et les transplantations, 
			représente une sous-spécialité, essentiellement exercée dans les centres 
			hospitalo-universitaires. De même, la chirurgie pancréatique, œsophagienne, 
			et rectale sous-péritonéale tend à se spécialiser et à être exercée dans des
			centres experts.</p>
