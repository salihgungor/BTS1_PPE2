		<h1 id="Neurogirurgie">Neurochirurgie</h1>
		<p>La neurochirurgie est la discipline chirurgicale qui est spécialisée dans la 
			chirurgie du système nerveux central et du système nerveux périphérique. Le 
			chirurgien spécialisé pratiquant la neurochirurgie s'appelle le 
			neurochirurgien.</p>
		<h3>Pathologies concernées</h3>
		<ul>
			<p>Parmi les états traités par les neurochirurgiens :<p/>
			<li>La hernie discale, qui est une saillie que fait un disque 
				intervertébral dans le canal rachidien, qui correspond à 
				l'expulsion en arrière de son noyau gélatineux, le nucleus pulposus et 
				causant une compression médullaire ou radiculaire (en commun avec les 
				orthopédistes)</li>
			<li>l'adénome hypophysaire, qui est une tumeur bénigne touchant 
				l'hypophyse et provoquant des troubles endocriniens ou neurologiques</li>
			<li>l'hydrocéphalie, qui est définie par l'augmentation du volume des 
				espaces contenant le liquide céphalo-rachidien</li>
			<li>le traumatisme crânien, qui recouvre l'ensemble des traumatismes du 
				neurocrâne (partie haute du crâne contenant le cerveau) et du cerveau</li>
			<li>le traumatisme du rachis avec les fractures vertébrales (en commun 
				avec les orthopédistes)</li>
			<li>la tumeur du cerveau et du cervelet </li>
			<li>la maladie de Parkinson, qui est une maladie neurologique chronique 
				affectant le système nerveux central responsable de troubles 
				essentiellement moteurs d'évolution progressive</li>
			<li>l'hyperacousie, qui est un dysfonctionnement de l'audition, 
				caractérisé par une hypersensibilité de l'ouïe</li>
			<li>les tumeurs cranio-cérébrales et extra-parenchymateuses</li>
		</ul>
