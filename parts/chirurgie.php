		<h1 id="chirurgieAm">Chirurgie ambulatoire</h1>
		<h3>Définition</h3>
		<p>La chirurgie ambulatoire est un mode de prise en charge permettant de 
			raccourcir à une seule journée votre hospitalisation pour une intervention 
			chirurgicale. Vous rentrez donc le matin pour être opéré le jour même et 
			ressortir le soir. La durée de séjour à l'hôpital est de quelques heures à 
			moins de 12 heures.Cette modalité de chirurgie est pratiquée avec l’accord du 
			patient et sous certaines conditions, tenant compte à la fois du type 
			d’intervention chirurgicale réalisée, des conditions de vie du patient 
			(présence d’un accompagnant) et de l’organisation des services de chirurgie.</p>
		<h3>Opérations concernées</h3>
		<p>Les interventions les plus courantes sont l’ablation de la cataracte, les 
			extractions dentaires (dents de sagesse), la cure de varices, les 
			arthroscopies, la cure des hernies.Des interventions de plus en plus complexes
			sont aussi réalisées en ambulatoire : l’ablation de la vésicule biliaire, 
			certaines interventions gynécologiques, le traitement de ligament du genou, 
			de pathologie de la main, de l’épaule…Et certains établissements de soins 
			commencent à pratiquer des interventions plus audacieuses encore, comme 
			l’ablation du côlon ou du lobe du poumon pour cancer, l’appendicectomie, la 
			pose d’une prothèse de genou et/ou de hanche, d’un by-pass pour les personnes 
			obèses…  </p>
		<p>En cancérologie, la chirurgie ambulatoire est déjà développée pour certains 
			organes, et notamment pour la chirurgie conservatrice du sein. Dans les 
			<a href="http://www.unicancer.fr/le-reseau-des-centres-de-lutte-contre-le-cancer" target="_self">
				centres de lutte contre le cancer</a>,
			17 % des patientes opérées pour un cancer du sein en ont bénéficié en 2012.</p>
