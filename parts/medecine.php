	<div class="blocks">
		<h1>Dermatologie</h1>
		<p>La dermatologie est la branche de la médecine qui s'occupe de la peau, des 
			muqueuses, des ongles cheveux et poils. elle est associée traditionnellement
			a l'étude des maladies vénériennes ou des MST. Le médecin spécialisé 
			pratiquant la dermatologie s'appelle le dermatologue</p>
	</div>
	<div class="blocks">
		<h1>Endocrinologie</h1>
	<p>L'endocrinologie est la science de la médecine qui étudie les hormones. 
		Son nom signifie : l'étude (logos) de la sécrétion (crine) interne (endo). 
		Elle étudie de très nombreux phénomènes physiologiques, car les hormones 
		interviennent dans de nombreuses fonctions chez de nombreux organismes dont
		l'Homme</p>
	</div>
	
	<div class="blocks">
		<h1>Médecine Gériatrique</h1>
	<p>La gériatrie est la médecine des personnes âgées, alors que la gérontologie
		désigne l'étude du vieillissement dans toutes ses dimensions, notamment 
		sociale, économique, démographique, psychologique, anthropologique, 
		culturelle, médicale et autres. La gériatrie est donc une des composantes 
		de la gérontologie.</p>
	<p>Plus précisément, la médecine gériatrique est la spécialité médicale 
		concernée par les affections physiques, mentales, fonctionnelles et sociales
		des malades âgés, en particulier lors de soins aigus, chroniques, de 
		réhabilitation, de prévention et en fin de vie. Ce groupe de patients 
		présente en général de multiples pathologies évolutives requérant une 
		approche globale. Les affections peuvent se présenter différemment avec 
		l’âge avancé, leur diagnostic et la réponse au traitement sont souvent 
		difficiles, et le besoin de soutien médico-psycho-social nécessaire</p>
	</div>
