		<h1>Cardiologie</h1>
		<p>La cardiologie est la spécialité médicale qui étudie le cœur et ses 
			maladies. Le médecin spécialisé pratiquant la cardiologie s'appelle le 
			cardiologue. Par extension, il s'intéresse également aux problèmes 
			vasculaires.</p>
	</div>
	<div class="blocks">
		<h1>Neurologie</h1>
		<p>La neurologie est la discipline médicale clinique qui étudie l'ensemble
			des maladies du système nerveux et en particulier du cerveau. Cette 
			spécialité médicale s'est séparée de la psychiatrie au XIXe siècle avec 
			l'école de Charcot à la Pitié-Salpêtrière. Le terme de Neurologie a été 
			introduit dans le vocabulaire médical par le médecin et anatomiste 
			anglais Thomas Willis. Le médecin spécialisé pratiquant la neurologie 
			s'appelle le neurologue.</p>
	</div>
	<div class="blocks">
		<h1>Nephrologie</h1>
		<p>La néphrologie est la spécialité médicale visant à prévenir, 
			diagnostiquer et soigner les maladies des reins. Elle est différente de 
			l'urologie, spécialité chirurgicale s'intéressant à l'appareil génital 
			masculin et à l'ensemble du système urinaire (reins, uretères, vessie, 
			prostate, urètre).</p>
		<p>Le mot néphrologie vient des mots grecs nephros (rein) et logos (étude)
			.</p>
		<p>De très nombreuses maladies affectant les reins ne sont pas limitées à 
			cet organe, mais sont des maladies générales (athérosclérose, diabète, 
			etc.).</p>
	</div>
